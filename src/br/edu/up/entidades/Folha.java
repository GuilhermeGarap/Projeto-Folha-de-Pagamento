package br.edu.up.entidades;

	public class Folha {
		private String nome;
		private int mes;
		private int ano;
		private int numHoras;
		private float valHoras;
		private float sb;
		private double ir;
		private double inss;
		private float fgts;
		private double sl;	
		
		//Gets e Sets


		public void setNome(String nome) {
			this.nome = nome;
		}

		public String getNome() {
			return nome;
		}

		public int getMes() {
			return mes;
		}

		public void setMes(int mes) {
			this.mes = mes;
		}

		public int getAno() {
			return ano;
		}

		public void setAno(int ano) {
			this.ano = ano;
		}

		public int getNumHoras() {
			return numHoras;
		}

		public void setNumHoras(int numHoras) {
			this.numHoras = numHoras;
		}

		public float getValHoras() {
			return valHoras;
		}

		public void setValHoras(float valHoras) {
			this.valHoras = valHoras;
		}	
	
		public float getSb() {
			return sb;
		}

		public void setSb(float sb) {
			this.sb = sb;
		}

		public double getIr() {
			return ir;
		}

		public void setIr(double ir) {
			this.ir = ir;
		}

		public double getInss() {
			return inss;
		}	

		public void setInss(double inss) {
			this.inss = inss;
		}
		
		public float getFgts() {
			return fgts;
		}

		public void setFgts(float fgts) {
			this.fgts = fgts;
		}

		public double getSl() {
			return sl;
		}
	
		public void setSl(double sl) {
			this.sl = sl;
		}
		}
		

		
